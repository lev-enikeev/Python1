import random

while True:
    s = input("Введите: ")
    if s=="рандомное число":
        print (random.randint(0,100))        
    elif s=="рандомное слово":
        print (random.choice(["привет","пока","лес","велосипед","телеграм","сердце","маинкравт","папа","мама","сестра","самолет","компютр","телефон","собака"]), random.randint(0,100),"%")

    else:
        print("по моему произошла ошибка")
